    public void leitura() {
        tableModel = (DefaultTableModel) tblCarros.getModel();
        tableModel.setNumRows(0);
        CarrosDAO carrosDAO = new CarrosDAO();
        List<Carros> carros = carrosDAO.leitura();

        for (Carros objCarros : carros) {
            Object[] rowData = {objCarros.getIdCarro(), objCarros.getMarca(), objCarros.getModelo(),objCarros.getAno(),objCarros.getCor(),objCarros.getPlaca(),objCarros.getStatus()};
            tableModel.addRow(rowData);
        }
    }
