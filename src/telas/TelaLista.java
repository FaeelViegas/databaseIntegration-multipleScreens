    private void btnStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStatusActionPerformed
        Carros objCarros = new Carros();
        String status = (String)cbxStatus.getSelectedItem();
        if(status.equals("Selecionar")){
            JOptionPane.showMessageDialog(null, "Selecione um status para o campo selecionado!");
            return;
        }
        if(id == 0){
            JOptionPane.showMessageDialog(null, "Selecione um campo!");
        }else{
            objCarros.setIdCarro(id);
            objCarros.setStatus(status);
            CarrosDAO carrosDao = new CarrosDAO();
            carrosDao.alterarCompos(objCarros);
            leitura();
        }
        id = 0;
    }//GEN-LAST:event_btnStatusActionPerformed

